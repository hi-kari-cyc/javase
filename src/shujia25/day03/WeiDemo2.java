package shujia25.day03;

/*
    <<: 将数据的补码左移若干位，高位溢出舍去，低位补0，左移就是乘上2的移动位次方
    >>: 将数据的补码向右移动若干位，低位溢出舍去，高位是0就用0补齐，是1就用1补齐，右移其实就是除以2的n次方
    >>>: 将数据的补码向右移若干位，低位溢出舍去，无论最高位是0还是1，都用0补齐

 */
public class WeiDemo2 {
    public static void main(String[] args) {
        System.out.println(4<<2);  // 4*2^2==>16
        System.out.println(4<<3);  // 4*2^3==>32
        System.out.println(-4<<2); // -4*2^2==>-16

        System.out.println(12>>2);  // 12/2^2==>3
        System.out.println(32>>3);  // 32/2^3==>4
        System.out.println(-12>>2); // -12/2^2==>-3
        System.out.println(-4>>>2); //1073741823
    }
}
